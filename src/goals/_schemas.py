"""Pydantic schemas for the goal proposal system."""

from enum import Enum
from typing import Dict, List, Optional

from pydantic import BaseModel, Field


class GoalType(str, Enum):
    """Types of strategic goals the system can propose."""

    PERFORMANCE_OPTIMIZATION = "performance_optimization"
    COST_REDUCTION = "cost_reduction"
    RELIABILITY_IMPROVEMENT = "reliability_improvement"
    WORKFLOW_RESTRUCTURING = "workflow_restructuring"
    CROSS_PRODUCT_OPPORTUNITY = "cross_product_opportunity"


class GoalStatus(str, Enum):
    """Lifecycle status for a goal proposal."""

    DRAFT = "draft"
    PROPOSED = "proposed"
    UNDER_REVIEW = "under_review"
    APPROVED = "approved"
    REJECTED = "rejected"
    DEFERRED = "deferred"
    IN_PROGRESS = "in_progress"
    COMPLETED = "completed"
    CANCELLED = "cancelled"


class GoalRiskLevel(str, Enum):
    """Risk level for a proposed change."""

    LOW = "low"
    MEDIUM = "medium"
    HIGH = "high"
    CRITICAL = "critical"


class EffortLevel(str, Enum):
    """Effort level for implementing a proposal."""

    TRIVIAL = "trivial"
    SMALL = "small"
    MEDIUM = "medium"
    LARGE = "large"
    MAJOR = "major"


class RiskAssessment(BaseModel):
    """Risk analysis for a goal proposal."""

    level: GoalRiskLevel = GoalRiskLevel.LOW
    blast_radius: str = ""
    reversible: bool = True
    safety_concerns: List[str] = Field(default_factory=list)
    mitigation_steps: List[str] = Field(default_factory=list)


class ImpactEstimate(BaseModel):
    """Expected impact of implementing a goal."""

    metric_name: str
    current_value: float
    expected_value: float
    improvement_pct: float = 0.0
    confidence: float = 0.5  # noqa: scanner: skip-magic


class GoalEvidence(BaseModel):
    """Evidence supporting a goal proposal."""

    pattern_ids: List[str] = Field(default_factory=list)
    workflow_ids: List[str] = Field(default_factory=list)
    metrics: Dict[str, object] = Field(default_factory=dict)
    analysis_summary: str = ""


class GoalProposal(BaseModel):
    """A strategic goal proposal generated by an analyzer."""

    goal_type: GoalType
    title: str
    description: str
    risk_assessment: RiskAssessment = Field(default_factory=RiskAssessment)
    effort_estimate: EffortLevel = EffortLevel.MEDIUM
    expected_impacts: List[ImpactEstimate] = Field(default_factory=list)
    evidence: GoalEvidence = Field(default_factory=GoalEvidence)
    source_product_type: Optional[str] = None
    applicable_product_types: List[str] = Field(default_factory=list)
    proposed_actions: List[str] = Field(default_factory=list)
    priority_score: float = 0.0


class GoalReviewAction(str, Enum):
    """Actions available during goal review."""

    APPROVE = "approve"
    REJECT = "reject"
    DEFER = "defer"


class GoalReview(BaseModel):
    """A review decision on a goal proposal."""

    action: GoalReviewAction
    reviewer: str
    reason: Optional[str] = None
    defer_until: Optional[str] = None
