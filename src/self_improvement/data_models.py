"""
Data models for M5 Self-Improvement system.

Contains models for agent configuration, performance profiles, improvement proposals,
experiments, and deployment tracking.
"""
from dataclasses import dataclass, field
from datetime import datetime
from typing import Any, Dict, List, Optional

from src.observability.datetime_utils import utcnow


@dataclass
class AgentPerformanceProfile:
    """
    Agent performance profile data model.

    Contains aggregated metrics for an agent over a time window. Generated by
    PerformanceAnalyzer through SQL queries against observability database.

    Metrics dictionary format:
    {
        "metric_name": {
            "mean": float,
            "std": float,      # standard deviation
            "p95": float,      # 95th percentile
            "p99": float,      # 99th percentile (optional)
        }
    }

    Example:
    {
        "success_rate": {"mean": 0.92, "std": 0.05},
        "cost_usd": {"mean": 0.80, "std": 0.12},
        "duration_seconds": {"mean": 42.5, "p95": 55.0, "std": 8.5},
        "quality_score": {"mean": 0.75, "std": 0.08},
    }

    Used by:
    - ImprovementDetector to compare current vs baseline performance
    - Dashboard/UI for displaying agent performance trends
    - Alerting system for detecting performance degradation
    """

    # Identity (required fields first)
    agent_name: str

    # Time window (required)
    window_start: datetime
    window_end: datetime

    # Execution count (required)
    total_executions: int

    # Optional fields with defaults
    profile_id: Optional[str] = None  # Auto-generated on storage

    # Aggregated metrics
    # Key: metric_name, Value: {"mean": X, "std": Y, "p95": Z, ...}
    metrics: Dict[str, Dict[str, float]] = field(default_factory=dict)

    # Metadata
    created_at: datetime = field(default_factory=utcnow)
    extra_metadata: Dict[str, Any] = field(default_factory=dict)

    def get_metric(self, metric_name: str, stat: str = "mean") -> Optional[float]:
        """Get a specific metric statistic."""
        metric_data = self.metrics.get(metric_name)
        if metric_data is None:
            return None
        return metric_data.get(stat)

    def has_metric(self, metric_name: str) -> bool:
        """Check if profile contains a metric."""
        return metric_name in self.metrics

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for database storage."""
        return {
            "profile_id": self.profile_id,
            "agent_name": self.agent_name,
            "window_start": self.window_start.isoformat(),
            "window_end": self.window_end.isoformat(),
            "total_executions": self.total_executions,
            "metrics": self.metrics,
            "created_at": self.created_at.isoformat(),
            "extra_metadata": self.extra_metadata,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "AgentPerformanceProfile":
        """Load from dictionary (e.g., from database)."""
        return cls(
            profile_id=data.get("profile_id"),
            agent_name=data["agent_name"],
            window_start=datetime.fromisoformat(data["window_start"]),
            window_end=datetime.fromisoformat(data["window_end"]),
            total_executions=data["total_executions"],
            metrics=data.get("metrics", {}),
            created_at=datetime.fromisoformat(data.get("created_at", utcnow().isoformat())),
            extra_metadata=data.get("extra_metadata", {}),
        )


@dataclass
class SIOptimizationConfig:
    """
    Agent configuration data model.

    Represents the configurable parameters for an agent that M5 can optimize.
    This includes inference settings, prompt configuration, and tool settings.

    Used by:
    - ImprovementStrategy to generate config variants
    - ExperimentOrchestrator to test different configurations
    - ConfigDeployer to deploy winning configurations
    """

    # Identity
    agent_name: str
    agent_version: str = "1.0.0"

    # Inference configuration
    inference: Dict[str, Any] = field(default_factory=lambda: {
        "provider": "ollama",
        "model": "llama3.1:8b",
        "temperature": 0.7,
        "max_tokens": 2048,
        "top_p": 0.9,
        "base_url": None,
    })

    # Prompt configuration
    prompt: Dict[str, Any] = field(default_factory=lambda: {
        "template": None,
        "include_examples": False,
        "num_examples": 0,
        "include_reasoning_guide": False,
        "include_citations": False,
        "require_source_verification": False,
        "sections": [],
    })

    # Tool configuration
    tools: Dict[str, Any] = field(default_factory=lambda: {
        "enabled_tools": [],
        "tool_timeout_seconds": 60,
        "max_retries": 3,
        "safety_checks": True,
    })

    # Caching configuration
    caching: Dict[str, Any] = field(default_factory=lambda: {
        "enabled": False,
        "cache_type": "memory",
        "ttl_seconds": 3600,
    })

    # Retry configuration
    retry: Dict[str, Any] = field(default_factory=lambda: {
        "max_retries": 3,
        "backoff_multiplier": 2,
        "initial_delay_seconds": 1,
    })

    # Metadata
    extra_metadata: Dict[str, Any] = field(default_factory=dict)

    def copy(self) -> "SIOptimizationConfig":
        """Create a deep copy of this config for generating variants."""
        import copy
        return SIOptimizationConfig(
            agent_name=self.agent_name,
            agent_version=self.agent_version,
            inference=copy.deepcopy(self.inference),
            prompt=copy.deepcopy(self.prompt),
            tools=copy.deepcopy(self.tools),
            caching=copy.deepcopy(self.caching),
            retry=copy.deepcopy(self.retry),
            extra_metadata=copy.deepcopy(self.extra_metadata),
        )

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for database storage."""
        return {
            "agent_name": self.agent_name,
            "agent_version": self.agent_version,
            "inference": self.inference,
            "prompt": self.prompt,
            "tools": self.tools,
            "caching": self.caching,
            "retry": self.retry,
            "extra_metadata": self.extra_metadata,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "SIOptimizationConfig":
        """Load from dictionary (e.g., from database)."""
        return cls(
            agent_name=data["agent_name"],
            agent_version=data.get("agent_version", "1.0.0"),
            inference=data.get("inference", {}),
            prompt=data.get("prompt", {}),
            tools=data.get("tools", {}),
            caching=data.get("caching", {}),
            retry=data.get("retry", {}),
            extra_metadata=data.get("extra_metadata", {}),
        )


@dataclass
class SelfImprovementExperiment:
    """
    Experiment data model for A/B/C/D testing configurations.

    Represents a running or completed experiment that tests multiple configuration
    variants against a control to find optimal agent settings.

    Used by:
    - ExperimentOrchestrator to create and manage experiments
    - StatisticalAnalyzer to analyze results and pick winners
    - ExperimentAssignment to route executions to variants
    """

    # Identity
    id: str
    agent_name: str

    # Status
    status: str  # 'running', 'completed', 'failed'

    # Configuration being tested
    control_config: SIOptimizationConfig
    variant_configs: List[SIOptimizationConfig]

    # Metadata
    proposal_id: Optional[str] = None
    created_at: datetime = field(default_factory=utcnow)
    completed_at: Optional[datetime] = None
    extra_metadata: Dict[str, Any] = field(default_factory=dict)

    def get_all_configs(self) -> Dict[str, SIOptimizationConfig]:
        """
        Get all configurations (control + variants) as a dictionary.

        Returns:
            Dict mapping variant_id to config:
            {
                "control": <SIOptimizationConfig>,
                "variant_0": <SIOptimizationConfig>,
                "variant_1": <SIOptimizationConfig>,
                ...
            }
        """
        configs = {"control": self.control_config}
        for i, variant in enumerate(self.variant_configs):
            configs[f"variant_{i}"] = variant
        return configs

    def get_variant_count(self) -> int:
        """Get total number of variants (including control)."""
        return 1 + len(self.variant_configs)

    def is_running(self) -> bool:
        """Check if experiment is still running."""
        return self.status == "running"

    def is_completed(self) -> bool:
        """Check if experiment has completed."""
        return self.status == "completed"

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for database storage."""
        return {
            "id": self.id,
            "agent_name": self.agent_name,
            "status": self.status,
            "control_config": self.control_config.to_dict(),
            "variant_configs": [v.to_dict() for v in self.variant_configs],
            "proposal_id": self.proposal_id,
            "created_at": self.created_at.isoformat(),
            "completed_at": self.completed_at.isoformat() if self.completed_at else None,
            "extra_metadata": self.extra_metadata,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "SelfImprovementExperiment":
        """Load from dictionary (e.g., from database)."""
        return cls(
            id=data["id"],
            agent_name=data["agent_name"],
            status=data["status"],
            control_config=SIOptimizationConfig.from_dict(data["control_config"]),
            variant_configs=[SIOptimizationConfig.from_dict(v) for v in data.get("variant_configs", [])],
            proposal_id=data.get("proposal_id"),
            created_at=datetime.fromisoformat(data["created_at"]),
            completed_at=datetime.fromisoformat(data["completed_at"]) if data.get("completed_at") else None,
            extra_metadata=data.get("extra_metadata", {}),
        )


